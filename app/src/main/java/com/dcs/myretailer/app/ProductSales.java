package com.dcs.myretailer.app;

public class ProductSales {
    String QtySold;
    String Amount;

    public ProductSales() {
    }

    public String getQtySold() {
        return QtySold;
    }

    public void setQtySold(String qtySold) {
        QtySold = qtySold;
    }

    public String getAmount() {
        return Amount;
    }

    public void setAmount(String amount) {
        Amount = amount;
    }
}
